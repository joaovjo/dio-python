print(500 == 200) # compara se o valor é igual ao outro
print(500 != 200) # compara se o valor é diferente do outro
print(500 >= 200) # compara se o valor é maior ou igual ao outro
print(500 <= 200) # compara se o valor é menor ou igual ao outro
print(500 > 200) # compara se o valor é maior que o outro
print(500 < 200) # compara se o valor é menor que o outro
print(500 is 500) # compara se é o mesmo tipo e valor do outro
saldo = 500  # Define o saldo inicial como 500
saldo -= 100  # Subtrai 100 do saldo
print(saldo)  # 400

saldo = 500  # Redefine o saldo inicial como 500
saldo += 100  # Adiciona 100 ao saldo
print(saldo)  # 600

saldo = 500  # Redefine o saldo inicial como 500
saldo *= 100  # Multiplica o saldo por 100
print(saldo)  # 50000

saldo = 500  # Redefine o saldo inicial como 500
saldo /= 100  # Divide o saldo por 100
print(saldo)  # 5.0

saldo = 500  # Redefine o saldo inicial como 500
saldo %= 100  # Calcula o resto da divisão do saldo por 100
print(saldo)  # 0

saldo = 500  # Redefine o saldo inicial como 500
saldo **= 2  # Eleva o saldo à potência de 2
print(saldo)  # 250000


lista = ["p", "y", "t", "h", "o", "n"]

# Além de strings, podemos fatiar listas usando o mesmo conceito de start, stop e step

print(lista[2:])  # A partir do índice 2
print(lista[:2])  # Até o índice 2
print(lista[1:4])  # Do índice 1 ao 4
print(lista[::2])  # De 2 em 2
print(lista[::-1])  # Inverte a lista
print(lista[0:3:2])  # Do índice 0 ao 3 de 2 em 2
print(lista[::])  # Toda a lista

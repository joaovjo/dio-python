matriz = [
    [1, 2, 3],
    ["b", "c", "d"],
    ["carro", "moto", "bicicleta"]
]

matriz[0] # Seleciona a primeira lista da matriz, nesse caso: [1, 2, 3]
print(matriz[0]) # [1, 2, 3]

matriz[0][0] # Seleciona o primeiro elemento da primeira lista, nesse caso: 1
print(matriz[0][0]) # 1

matriz[1] # Seleciona a segunda lista da matriz, nesse caso: ["b", "c", "d"]
print(matriz[1]) # ["b", "c", "d"]

matriz[1][1] # Seleciona o segundo elemento da segunda lista, nesse caso: "c"
print(matriz[1][1]) # c

matriz[2] # Seleciona a terceira lista da matriz, nesse caso: ["carro", "moto", "bicicleta"]
print(matriz[2]) # ["carro", "moto", "bicicleta"]

matriz[2][2] # Seleciona o terceiro elemento da terceira lista, nesse caso: "bicicleta"
print(matriz[2][2]) # bicicleta